// Function to generate a random date within the last 30 or 60 days
function generateRandomDate() {
  const days = Math.random() < 0.5 ? 30 : 60; // 50% chance for 30 or 60 days
  return new Date(
    Date.now() - Math.random() * days * 24 * 60 * 60 * 1000
  ).toISOString();
}

